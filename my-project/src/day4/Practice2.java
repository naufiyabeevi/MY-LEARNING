package day4;

public class Practice2 {
    public static void main(String[] args) {
        //write a java program using while loop print 1to 5 natural numbers?
        int natural=1;
        while (natural<=5){
            System.out.println(natural);
            natural++;
        }
        System.out.println("\n");

        //write a java program using do loop print 1to 7 natural numbers?
        int value=1;
        do {
            System.out.println(value);
            value++;
        }while(value<=7);
        System.out.println("\n");
        //write a java program using do loop print 1to 6 natural numbers?
        for(int k=1;k<=6;k++){
            System.out.println(k);
        }
    }}

