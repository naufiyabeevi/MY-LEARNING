package day6;

class Calculator {
    //user defined method
    //instance method
    int addition(int a, int b) {
        int sum = a + b;
        return sum;
    }

    int multiplication(int e, int f) {
        int mult = e * f;
        return mult;
    }


    int substraction(int c, int d) {
        int difference = c - d;
        return difference;
    }

    float division(float i, float m) {
        float div = i / m;
        return div;
    }
}
