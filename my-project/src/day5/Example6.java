package day5;

public class Example6 {
    public static void main(String[] args) {
        //write a program to find sum of natural numbers using for loop?
        int num = 5;//1+2+3+4+5
        int sum=0;
        for (int m = 0; m <= num; m++) {
            sum =sum+ m;//0 1 3 6 10 15
        }
        System.out.println(sum);
    }
}
