package day9;

//Encapsulation
public class Example2 {

    private int number ; // declare class variables/attributes as private

    //There should be getters and setters

    // Geeter method
    // The get method returns the value of the variable number.
    public int getNumber() {
        return number;
    }

    // Setter method
    // The set method takes a parameter and assigns it to the number variable.
    public void setNumber(int n) {
        number = n;
    }

    // We achieved encapsulation!
}
